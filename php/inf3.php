                <h5 class="card-title">ways to meditate</h5>
                <p>Meditation is simpler (and harder) than most people think. Read these steps, make sure you’re
                    somewhere where you can relax into this process, set a timer, and give it a shot:

                    1) Take a seat
                    Find place to sit that feels calm and quiet to you.
                    <br>

                    2) Set a time limit
                    If you’re just beginning, it can help to choose a short time, such as five or 10 minutes.
                    <br>

                    3) Notice your body
                    You can sit in a chair with your feet on the floor, you can sit loosely cross-legged, you can
                    kneel—all are fine. Just make sure you are stable and in a position you can stay in for a while.
                    <br>

                    4) Feel your breath
                    Follow the sensation of your breath as it goes in and as it goes out.

                    <br>

                    5) Notice when your mind has wandered
                    Inevitably, your attention will leave the breath and wander to other places. When you get around to
                    noticing that your mind has wandered—in a few seconds, a minute, five minutes—simply return your
                    attention to the breath.

                    <br>
                    6) Be kind to your wandering mind
                    Don’t judge yourself or obsess over the content of the thoughts you find yourself lost in. Just come
                    back.

                    <br>
                    7) Close with kindness
                    When you’re ready, gently lift your gaze (if your eyes are closed, open them). Take a moment and
                    notice any sounds in the environment. Notice how your body feels right now. Notice your thoughts and
                    emotions.

                    <br>

                    That’s it! That’s the practice. You focus your attention, your mind wanders, you bring it back, and
                    you try to do it as kindly as possible (as many times as you need to).
                </p>
